// The Decorator Pattern is a structural design pattern
// that allows you to add new behavior to an object dynamically,
// without altering its structure. This pattern is useful
// when you need to extend the functionality of an object in a flexible
// and reusable way.

// Key Concepts:
// The Decorator pattern allows you to attach additional responsibilities to
// an object at runtime.
// It is often used to add features to individual objects,
// without affecting other objects of the same class.
// The main goal is to extend the behavior of objects in a flexible way by
//  "decorating" them with additional functionality.
