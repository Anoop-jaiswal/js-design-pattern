// In JavaScript, a design pattern is a reusable solution to a commonly occurring problem in software design.
// It provides a general template or blueprint for solving a specific type of problem
// that can be customized and applied in different situations

//Types of Design Patterns:
//There are three types of design pattern
//1 - creational
//2 - Structural
//3 - Behavioral

// Creational:

// These patterns focus on object creation mechanisms, providing ways to create objects in a flexible and efficient manner.
//  Examples include:

//The Constructor Pattern is a design pattern in JavaScript that involves defining a constructor
// function which can be used to create objects with similar properties and methods.
// The constructor function acts as a blueprint or template for creating new objects of the same type.

// Singleton: Ensures that only one instance of a class exists.

// Builder: Constructs complex objects step by step.

// Factory Method: Creates objects without specifying the exact class to create.

// Abstract Factory Pattern : The Abstract Factory pattern allows us to produce families of
// related objects without specifying concrete classes.

// Prototype Pattern
// The Prototype pattern allows you to create an object using
// another object as a blueprint, inheriting its properties and methods.
